import math as M  
Radius = float (input ("Please enter the radius of the given circle: "))  
area_of_the_circle = M.pi* Radius * Radius  
print (" The area of the given circle is: ", area_of_the_circle)

height = float (input ("Please enter the height of the triangle: "))  
base = float (input ("Please enter the base of the triangle: "))  
area_of_the_triangle = 1/2 * height * base  
print (" The area of the given circle is: ", area_of_the_triangle)